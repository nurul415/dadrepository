package payment;

public class Staff 
{
	private String name;
	private String userId;
	
	public Staff()
	{
		
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	public String getName()
	{
		return name;
	}
	
	public void setUserId(String userId)
	{
		this.userId = userId;
	}
	public String getUserId()
	{
		return userId;
	}
	
}
